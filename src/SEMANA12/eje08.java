class node {

    int dato;
    node sgte;
    node ant;

    public node(int n) {
        this.dato = n;
        sgte = ant = null;
    }

    int tostring() {
        return this.dato;
    }
}

class metodos {

    node inicio;
    node fin;

    public metodos() {
        inicio = fin = null;
    }

    void ingresar(int n) {
        node newnodo = new node(n);
        if (inicio == null) {
            inicio = fin = newnodo;
        } else {
            fin.sgte = newnodo;
            newnodo.ant = fin;
            fin = newnodo;
        }
    }

    String mostrar() {
        String cad = "";
        node temp = inicio;
        while (temp != null) {
            cad += temp.tostring() + "\n";
            temp = temp.sgte;
        }
        return cad;
    }

    void insertar(int n, int p) {
        node newnodo = new node(n);
        node temp1 = null, temp2 = null;
        if (inicio != null) {
            temp1 = inicio;
            temp2 = temp1.sgte;
        }
        int i = 1;
        if (p > (nodosize() + 1)) {
            JOptionPane.showMessageDialog(null, "Fuera de rango!!");
        } else {
            if (p < 0) {
                JOptionPane.showMessageDialog(null, "Se ingreso un Rango Negativo!!");
            } else {
                if (p == 1) {
                    inicio.ant = newnodo;
                    newnodo.sgte = inicio;
                    inicio = newnodo;
                } else {
                    if (p == nodosize() + 1) {
                        fin.sgte = newnodo;
                        newnodo.ant = fin;
                        fin = newnodo;
                    } else {
                        while (temp1 != null) {
                            //System.out.println("entro");
                            if (i == (p - 1)) {
                                temp1.sgte = newnodo;
                                temp2.ant = newnodo;
                                newnodo.ant = temp1;
                                newnodo.sgte = temp2;
                                break;
                            }
                            temp1 = temp1.sgte;
                            temp2 = temp1.sgte;
                            i++;
                        }
                    }
                }
            }

        }

    }

    int nodosize() {
        int x = 0;
        node temp = inicio;
        while (temp != null) {
            temp = temp.sgte;
            x++;
        }
        return x;
    }
}

