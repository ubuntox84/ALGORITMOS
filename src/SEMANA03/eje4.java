public class eje4 {
    public static void main(String[] args) {
        String op="+";
        switch(op){
            case "+":{System.out.println("Sumar");
                break;
            }
            case "-":{System.out.println("Resta");
                break;
            }
            case "*": {System.out.println("Producto");
                break;
            }
            case "/":{System.out.println("Divisio");
                break;
            }
            default:System.out.println("Signo desconocido!!");
            
        }
    }
}
